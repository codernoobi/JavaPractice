public class MathMethods {
    public static void main(String args[]){
        System.out.println(Math.abs(-26.7)+"=absolute value of -26.7");
        System.out.println(Math.max(8.6,8.7)+"=max of 8.6,8.7");
        System.out.println(Math.min(8.6,8.7)+"=min of 8.6,8.7");
        System.out.println(Math.pow(8,2)+"=pow of 8,2");
        System.out.println(Math.sqrt(9)+"=sqrt of 9");
        System.out.println(Math.ceil(8.6)+"=round up of 8.6");
        System.out.println(Math.floor(8.6)+"=round down of 8.6");
    }
}
